<h1>Historia de <strong>INFANTIL</strong>&nbsp;</h1>
<p class="lead">Un grupo de personas con vocaci&oacute;n para organizar y desarrollar un espacio de inserci&oacute;n social a trav&eacute;s de un inter&eacute;s com&uacute;n, en este caso la pr&aacute;ctica del futbol de sal&oacute;n, comenz&oacute; en 1984 con la organizaci&oacute;n de este singular evento que ha sido actividad fundante del desarrollo deportivo y social de nuestra Instituci&oacute;n.</p>
<p>Se ha generado no s&oacute;lo un beneficio directo para aquellos ni&ntilde;os que cuentan con un espacio organizado y acondicionado para la practica deportiva, insert&aacute;ndolos en la "competencia", las interrelaciones con sus pares, el ordenamiento de su documentaci&oacute;n personal, sino tambi&eacute;n se han multiplicado los beneficios en relaci&oacute;n a la Sociedad toda, generando asociaciones entre personas que se unen a los efectos de organizar la actividad de los distintos equipos que participan del Torneo: selecci&oacute;n de los ni&ntilde;os, actualizaci&oacute;n de documentaci&oacute;n, entrenamiento y preparaci&oacute;n para la competencia, servicios de traslado, provisi&oacute;n de indumentaria, control de descanso y alimentaci&oacute;n adecuada, seguimiento acad&eacute;mico, entre otros.</p>
<p>En el primer torneo oficial participaron 76 equipos representantes de distintos barrios de la ciudad m&aacute;s un equipo de Caleta Olivia.</p>
<p>En la actualidad son 144 equipos los participantes representando a la mayor&iacute;a de los barrios de Comodoro, Sarmiento y Las Heras. Anualmente se inscriben 1400 jugadores.</p>
<p>Alrededor de este movimiento deportivo trabajan al menos dos responsables por categor&iacute;a lo que suman unas 300 personas mayores que ofician de cuerpo t&eacute;cnico y son el nexo directo con la organizaci&oacute;n (comunicaci&oacute;n de horarios y estad&iacute;sticas); detr&aacute;s de ellas hay un n&uacute;mero importante de padres que colaboran con el cuerpo t&eacute;cnico en otras tareas.</p>
<p>En las jornadas propiamente dichas se vinculan 9 &aacute;rbitros oficiales por cada una de ellas, 6 planilleros estudiantes locales de distintas carreras, una persona encargada de la boleter&iacute;a, una en recepci&oacute;n de los equipos, una en el ordenamiento de la playa de estacionamiento y limpieza de los sanitarios p&uacute;blicos y una persona encargada de la coordinaci&oacute;n del certamen, la cual responde &iacute;ntegra y cabalmente al esp&iacute;ritu que el Consejo Directivo del club se fij&oacute; desde los inicios de la actividad.</p>
<p>Unas 2.000 personas (menores y mayores) circulan por el campeonato de manera directa como jugadores, cuerpo t&eacute;cnico y espectadores en cada jornada.</p>
<p>En la faz deportiva, los profesionales deportivos de nuestra casa ofician de buscadores de talentos, detectados se los proyecta en el f&uacute;tbol amateur de la C.A.I.</p>
